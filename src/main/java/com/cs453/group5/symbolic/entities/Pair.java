package com.cs453.group5.symbolic.entities;

/**
 * Binary tuple class which is usually a default feature for other languages.
 */
public class Pair<F, S> {
    private F first;
    private S second;

    public Pair(F first, S second) {
        this.first = first;
        this.second = second;
    }

    public F getFirst() {
        return first;
    }

    public S getSecond() {
        return second;
    }
}
